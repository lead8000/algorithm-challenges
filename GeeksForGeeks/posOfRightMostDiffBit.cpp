class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        bitset<32> bset_m(m), bset_n(n);
        for (size_t i = 0; i < 32; i++)
        {
            if (bset_m[i] != bset_n[i])
                return i+1;
        }
        return -1;
    }
};

